import { readFileSync, writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, resolve } from "node:path"
import { spawnSync } from "node:child_process"

const scriptDir = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(scriptDir, "..")

const sourceImage = resolve(repoRoot, "public/app-icon.png")
const outputDir = resolve(repoRoot, "public/pwa-assets")
const manifestPath = resolve(repoRoot, "public/manifest.json")
const indexPath = resolve(repoRoot, "public/pwa-assets.html")
const modulePath = resolve(repoRoot, "src/shared/lib/pwa-assets.ts")
const installGreen = "#014830"

const generatorArgs = [
  "exec",
  "pwa-asset-generator",
  sourceImage,
  outputDir,
  "--manifest",
  manifestPath,
  "--index",
  indexPath,
  "--scrape",
  "false",
]

runPwaGenerator([
  ...generatorArgs,
  "--icon-only",
  "--favicon",
  "--mstile",
  "--dark-mode",
  "--padding",
  "0",
  "--opaque",
  "false",
])
runPwaGenerator([
  ...generatorArgs,
  "--splash-only",
  "--background",
  installGreen,
  "--padding",
  "0",
])
normalizeManifest()
writeHeadModule()

function runPwaGenerator(args) {
  const result = spawnSync("pnpm", args, {
    cwd: repoRoot,
    stdio: "inherit",
  })

  if (result.status !== 0) {
    throw new Error(`PWA asset generation failed: ${result.error?.message ?? "unknown error"}`)
  }
}

function normalizeManifest() {
  const manifest = JSON.parse(readFileSync(manifestPath, "utf8"))

  manifest.name = "Zakat Companion"
  manifest.short_name = "Zakat"
  manifest.id = "/app"
  manifest.scope = "/"
  manifest.start_url = "/app/"
  manifest.display = "standalone"
  manifest.theme_color = installGreen
  manifest.background_color = installGreen

  writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`)
}

function writeHeadModule() {
  const html = readFileSync(indexPath, "utf8")
  const tags = extractTags(html)

  const linkTags = []
  const metaTags = []

  for (const tag of tags) {
    if (tag.kind === "link") {
      linkTags.push(tag.attrs)
      continue
    }

    if (tag.kind === "meta") {
      metaTags.push(tag.attrs)
    }
  }

  const contents = `// Generated by scripts/generate-pwa-assets.mjs. Do not edit by hand.
export type PwaLink = {
  rel: string
  href: string
  type?: string
  sizes?: string
  media?: string
}

export type PwaMeta = {
  name: string
  content: string
}

export const pwaLinks: PwaLink[] = ${JSON.stringify(linkTags, null, 2)}

export const pwaMeta: PwaMeta[] = ${JSON.stringify(metaTags, null, 2)}
`

  writeFileSync(modulePath, `${contents}\n`)
}

function extractTags(html) {
  const tags = []
  const tagRegex = /<(link|meta)\s+([^>]+?)\s*\/?>/g
  let match

  while ((match = tagRegex.exec(html))) {
    const kind = match[1]
    const attrs = parseAttributes(match[2])

    if (kind === "link") {
      if (!attrs.rel) {
        continue
      }

      if (attrs.rel === "icon" || attrs.rel === "apple-touch-icon" || attrs.rel === "apple-touch-startup-image") {
        attrs.href = normalizeHref(attrs.href)
        tags.push({ kind, attrs })
      }
      continue
    }

    if (kind === "meta") {
      if (attrs.name === "apple-mobile-web-app-capable" || attrs.name?.startsWith("msapplication-")) {
        if (attrs.content && attrs.content.startsWith("pwa-assets/")) {
          attrs.content = `/${attrs.content}`
        }
        tags.push({ kind, attrs })
      }
    }
  }

  return tags
}

function parseAttributes(source) {
  const attrs = {}
  const attrRegex = /([a-zA-Z0-9:-]+)="([^"]*)"/g
  let match

  while ((match = attrRegex.exec(source))) {
    attrs[match[1]] = match[2]
  }

  return attrs
}

function normalizeHref(href) {
  return href.startsWith("/") ? href : `/${href}`
}
